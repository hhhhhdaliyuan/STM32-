// 16x8 ASCII像素级旋转显示
void LCD_ShowChar_Rotate1608(uint16_t x, uint16_t y, char chr, uint16_t TextColor, uint16_t BackColor, uint16_t angle)
{
    const uint8_t *pFont;
    uint8_t row, col, temp;
    uint16_t px, py;

    if (chr < 32 || chr > 126) chr = '?';
    pFont = &Table1608[(chr - 32) * 16];

    for (row = 0; row < 16; row++)
    {
        temp = pFont[row];
        for (col = 0; col < 8; col++)
        {
            uint16_t color = (temp & (0x80 >> col)) ? TextColor : BackColor;
            switch(angle)
            {
                case 0:
                    px = x + col;
                    py = y + row;
                    break;
                case 90:
                    px = x + (15 - row);
                    py = y + col;
                    break;
                case 180:
                    px = x + (7 - col);
                    py = y + (15 - row);
                    break;
                case 270:
                    px = x + row;
                    py = y + (7 - col);
                    break;
                default:
                    px = x + col;
                    py = y + row;
            }
            LCD_DrawPoint(px, py, color);
        }
    }
}

// 多字符旋转显示
void LCD_ShowString_Rotate1608(uint16_t x, uint16_t y, const char *str, uint16_t TextColor, uint16_t BackColor, uint16_t angle)
{
    int len = strlen(str);
    int i;
    if(angle == 0) {
        // 正向，左到右
        for(i = 0; i < len; i++)
            LCD_ShowChar_Rotate1608(x + i * 8, y, str[i], TextColor, BackColor, 0);
    } else if(angle == 180) {
        // 倒置，右到左
        for(i = len - 1; i >= 0; i--)
            LCD_ShowChar_Rotate1608(x + (len - 1 - i) * 8, y, str[i], TextColor, BackColor, 180);
    } else if(angle == 90) {
        // 竖排，从下到上，顺序反转
        for(i = len - 1; i >= 0; i--)
            LCD_ShowChar_Rotate1608(x, y - (len - 1 - i) * 8, str[i], TextColor, BackColor, 90);
    } else if(angle == 270) {
        // 竖排，从上到下，顺序反转
        for(i = len - 1; i >= 0; i--)
            LCD_ShowChar_Rotate1608(x, y + (len - 1 - i) * 8, str[i], TextColor, BackColor, 270);
    }
}

void LCD_DrawLine(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2, uint16_t color)
{
    int dx = abs(x2 - x1);
    int dy = abs(y2 - y1);
    int sx = (x1 < x2) ? 1 : -1;
    int sy = (y1 < y2) ? 1 : -1;
    int err = dx - dy;

    while (1)
    {
        LCD_DrawPoint(x1, y1, color);

        if (x1 == x2 && y1 == y2)
            break;

        int e2 = 2 * err;
        if (e2 > -dy)
        {
            err -= dy;
            x1 += sx;
        }
        if (e2 < dx)
        {
            err += dx;
            y1 += sy;
        }
    }
}

//画人
void LCD_DrawMan(uint16_t x, uint16_t y, uint16_t size)
{
    uint16_t head_r = size / 4; // 头部半径
    uint16_t body_len = size / 2;
    uint16_t arm_len = size / 2;
    uint16_t leg_len = size / 2;

    // 1. 头部
    LCD_DrawCircle(x, y, head_r, COLOR_SKIN);

    // 2. 身体（蓝色T恤）
    LCD_DrawLine(x, y + head_r, x, y + head_r + body_len, COLOR_SHIRT);

    // 3. 左臂（蓝色T恤袖子）
    LCD_DrawLine(x, y + head_r + size/8, x - arm_len, y + head_r + body_len/2, COLOR_SHIRT);

    // 4. 右臂（蓝色T恤袖子）
    LCD_DrawLine(x, y + head_r + size/8, x + arm_len, y + head_r + body_len/2, COLOR_SHIRT);

    // 5. 左腿（深蓝色裤子）
    LCD_DrawLine(x, y + head_r + body_len, x - leg_len/2, y + head_r + body_len + leg_len, COLOR_PANTS);

    // 6. 右腿（深蓝色裤子）
    LCD_DrawLine(x, y + head_r + body_len, x + leg_len/2, y + head_r + body_len + leg_len, COLOR_PANTS);

    // 7. 左脚（黑色）
    LCD_DrawLine(x - leg_len/2, y + head_r + body_len + leg_len, x - leg_len/2, y + head_r + body_len + leg_len + size/8, COLOR_SHOES);

    // 8. 右脚（黑色）
    LCD_DrawLine(x + leg_len/2, y + head_r + body_len + leg_len, x + leg_len/2, y + head_r + body_len + leg_len + size/8, COLOR_SHOES);
}

