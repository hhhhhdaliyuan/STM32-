/*                void LCD_FSMC_Init ( void )操作寄存器                     */
void LCD_FSMC_Init_REGISTER ( void )
{
RCC_AHBPeriphClockCmd ( RCC_AHBPeriph_FSMC, ENABLE );
FSMC_Bank1->BTCR[0] = 0x00000000;    //FSMC_BCR1
FSMC_Bank1->BTCR[1] = 0x00000000;	  //FSMC_BTR1
FSMC_Bank1E->BWTR[0] = 0x00000000;	  //FSMC_BWTR1
		
FSMC_Bank1->BTCR[0] = 0x000050D9;    //FSMC_BCR1，不扩展0x000010D9，扩展0x000050D9
FSMC_Bank1->BTCR[1] = 0x10000F01;	  //FSMC_BTR1，读0x10000F01
FSMC_Bank1E->BWTR[0] = 0x10000301;	  //FSMC_BWTR1，写0x10000301
}



void LCD_FSMC_Init ( void )
{
FSMC_NORSRAMInitTypeDef  FSMC_NORSRAMInitStructure;
FSMC_NORSRAMTimingInitTypeDef  readWriteTiming; 	
RCC_AHBPeriphClockCmd ( RCC_AHBPeriph_FSMC, ENABLE );
//地址建立时间（ADDSET）为1个HCLK 2/72M = 28ns
readWriteTiming.FSMC_AddressSetupTime = 0x01;
//数据保持时间（DATAST）+ 1个HCLK = 5/72M=70ns	
readWriteTiming.FSMC_DataSetupTime  = 0x04;
//模式B,异步NOR FLASH模式，与ILI9341的8080时序匹配
readWriteTiming.FSMC_AccessMode  = FSMC_AccessMode_B;		
/*以下配置与模式B无关*/
//地址保持时间（ADDHLD）模式A未用到
readWriteTiming.FSMC_AddressHoldTime  = 0x00;
//设置总线转换周期，仅用于复用模式的NOR操作
readWriteTiming.FSMC_BusTurnAroundDuration = 0x00;
//设置时钟分频，仅用于同步类型的存储器
readWriteTiming.FSMC_CLKDivision  = 0x00;
//数据保持时间，仅用于同步型的NOR	
readWriteTiming.FSMC_DataLatency  = 0x00;		
FSMC_NORSRAMInitStructure.FSMC_Bank  = FSMC_Bank1_NORSRAM1;
FSMC_NORSRAMInitStructure.FSMC_DataAddressMux  = FSMC_DataAddressMux_Disable;
FSMC_NORSRAMInitStructure.FSMC_MemoryType = FSMC_MemoryType_NOR;
FSMC_NORSRAMInitStructure.FSMC_MemoryDataWidth = FSMC_MemoryDataWidth_16b;
FSMC_NORSRAMInitStructure.FSMC_BurstAccessMode = FSMC_BurstAccessMode_Disable;
FSMC_NORSRAMInitStructure.FSMC_WaitSignalPolarity = FSMC_WaitSignalPolarity_Low;
FSMC_NORSRAMInitStructure.FSMC_WrapMode = FSMC_WrapMode_Disable;
FSMC_NORSRAMInitStructure.FSMC_WaitSignalActive = FSMC_WaitSignalActive_BeforeWaitState;
FSMC_NORSRAMInitStructure.FSMC_WriteOperation = FSMC_WriteOperation_Enable;
FSMC_NORSRAMInitStructure.FSMC_WaitSignal = FSMC_WaitSignal_Disable;
FSMC_NORSRAMInitStructure.FSMC_ExtendedMode  = FSMC_ExtendedMode_Disable;
FSMC_NORSRAMInitStructure.FSMC_WriteBurst  = FSMC_WriteBurst_Disable;
FSMC_NORSRAMInitStructure.FSMC_ReadWriteTimingStruct = &readWriteTiming;
FSMC_NORSRAMInitStructure.FSMC_WriteTimingStruct = &readWriteTiming;  
FSMC_NORSRAMInit ( & FSMC_NORSRAMInitStructure ); 	
/* 使能 FSMC_Bank1_NORSRAM4 */
FSMC_NORSRAMCmd ( FSMC_Bank1_NORSRAM1, ENABLE );  		
}




