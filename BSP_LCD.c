void LcdDispEnchar1608(uint16_t X, uint16_t Y,uint8_t Enchar,uint16_t TextColor, uint16_t BackColor)
{
uint8_t byteCount;
uint8_t bitCount;
uint16_t RelativePosition;

RelativePosition = (Enchar - ' ') * 16;

LcdSetWindow(X,Y,8,16);
LCD_Write_Cmd(0x2C);
for(byteCount = 0; byteCount < 16; byteCount++)
      {
           for(bitCount = 0; bitCount < 8; bitCount++)
            {
            if(Table1608[RelativePosition + byteCount] & (0x80 >> bitCount))
            LCD_Write_Data(TextColor);
            else
             LCD_Write_Data(BackColor);
             }
       }
}



