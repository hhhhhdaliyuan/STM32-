//画小汽车
void LCD_DrawCar(uint16_t x, uint16_t y, uint16_t size)
{
    uint16_t body_w = size;
    uint16_t body_h = size / 3;
    uint16_t top_w  = size / 2;
    uint16_t top_h  = size / 4;
    uint16_t wheel_r = size / 8;

    // 1. 画车身
    LCD_DrawRectangle(x - body_w/2, y - body_h/2, x + body_w/2, y + body_h/2, COLOR_CAR_BODY);

    // 2. 画车顶
    LCD_DrawRectangle(x - top_w/2, y - body_h/2 - top_h, x + top_w/2, y - body_h/2, COLOR_CAR_TOP);

    // 3. 画左轮
    LCD_DrawCircle(x - body_w/3, y + body_h/2 + wheel_r, wheel_r, COLOR_WHEEL);

    // 4. 画右轮
    LCD_DrawCircle(x + body_w/3, y + body_h/2 + wheel_r, wheel_r, COLOR_WHEEL);

    // 5. 画车窗
    LCD_DrawRectangle(x - top_w/4, y - body_h/2 - top_h + 2, x + top_w/4, y - body_h/2 - 2, COLOR_WINDOW);
}

