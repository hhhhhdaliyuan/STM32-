void LCD_DrawCircle(uint16_t x0, uint16_t y0, uint16_t r, uint16_t color) {
    int x = 0, y = r;
    int d = 3 - 2 * r;
    while (x <= y) {
        LcdDrawPoint(x0 + x, y0 + y, color);
        LcdDrawPoint(x0 - x, y0 + y, color);
        LcdDrawPoint(x0 + x, y0 - y, color);
        LcdDrawPoint(x0 - x, y0 - y, color);
        LcdDrawPoint(x0 + y, y0 + x, color);
        LcdDrawPoint(x0 - y, y0 + x, color);
        LcdDrawPoint(x0 + y, y0 - x, color);
        LcdDrawPoint(x0 - y, y0 - x, color);
        if (d < 0)
            d = d + 4 * x + 6;
        else {
            d = d + 4 * (x - y) + 10;
            y--;
        }
        x++;
    }
}

// 实心圆
void LCD_FillCircle(uint16_t x0, uint16_t y0, uint16_t r, uint16_t color) {
    int x = 0, y = r;
    int d = 3 - 2 * r;
    while (x <= y) {
        LcdFill(x0 - x, y0 - y, 2 * x + 1, 1, color);
        LcdFill(x0 - y, y0 - x, 2 * y + 1, 1, color);
        LcdFill(x0 - x, y0 + y, 2 * x + 1, 1, color);
        LcdFill(x0 - y, y0 + x, 2 * y + 1, 1, color);
        if (d < 0)
            d = d + 4 * x + 6;
        else {
            d = d + 4 * (x - y) + 10;
            y--;
        }
        x++;
    }
}

